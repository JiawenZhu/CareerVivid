/**
 * Generates the lightweight guide metadata used by the Web catalog.
 *
 * The studio displays the full quest inventory, not just the raw source-guide
 * prompts. That inventory includes category-bank questions materialized for
 * every company, using the same sequence as the quest tracker and Firestore
 * seed scripts.
 *
 * Usage:
 *   node scripts/generate-interview-guide-summaries.mjs
 *   node scripts/generate-interview-guide-summaries.mjs --check
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const guideDirectory = path.join(root, 'data', 'interview-guides');
const bankPath = path.join(root, 'data', 'quest-category-banks.json');
const indexPath = path.join(guideDirectory, '_index.json');
const summariesPath = path.join(root, 'src', 'data', 'interviewGuideSummaries.generated.ts');
const interviewerBuckets = ['coding', 'systemDesign', 'behavioral', 'values'];
const tiers = ['easy', 'medium', 'hard'];

const banks = JSON.parse(fs.readFileSync(bankPath, 'utf8'));
const guides = fs.readdirSync(guideDirectory)
  .filter((filename) => filename.endsWith('.json') && !filename.startsWith('_'))
  .map((filename) => JSON.parse(fs.readFileSync(path.join(guideDirectory, filename), 'utf8')))
  .filter((guide) => guide.company && guide.slug)
  .sort((left, right) => left.company.localeCompare(right.company));

const interviewerQuestionCount = (guide) => interviewerBuckets.reduce(
  (count, bucket) => count + (guide.sampleQuestions?.[bucket]?.length ?? 0),
  0,
);

const categoryQuestions = (guide, stage) => {
  const category = banks.companyCategory?.[guide.slug];
  if (!category) throw new Error(`Missing category assignment for ${guide.slug}.`);
  return tiers.flatMap((tier) =>
    (banks.questionBanks?.[category]?.[stage]?.[tier] ?? [])
      .map((question) => String(question).replace(/\{company\}/g, guide.company)),
  );
};

const questQuestionCount = (guide) => {
  const samples = guide.sampleQuestions ?? {};
  const behavioral = samples.behavioral ?? [];
  const values = samples.values ?? [];
  return [
    ...categoryQuestions(guide, 'screening'),
    ...(samples.coding ?? []),
    ...(samples.systemDesign ?? []),
    ...behavioral,
    ...categoryQuestions(guide, 'behavioral').filter((question) => !behavioral.includes(question)),
    ...values,
    ...categoryQuestions(guide, 'values').filter((question) => !values.includes(question)),
    ...categoryQuestions(guide, 'final'),
  ].length;
};

const summaries = guides.map((guide) => ({
  company: guide.company,
  slug: guide.slug,
  scrapedAt: guide.scrapedAt ?? '',
  stageCount: guide.interviewStages?.length ?? 0,
  questionCount: interviewerQuestionCount(guide),
  tipCount: guide.tips?.length ?? 0,
  difficulty: guide.difficulty ?? null,
  topics: [
    ...(guide.codingTopics ?? []),
    ...(guide.systemDesignTopics ?? []),
    ...(guide.behavioralTopics ?? []),
  ].slice(0, 3),
}));

const totals = {
  companies: summaries.length,
  stages: summaries.reduce((count, guide) => count + guide.stageCount, 0),
  questions: summaries.reduce((count, guide) => count + guide.questionCount, 0),
  questQuestions: guides.reduce((count, guide) => count + questQuestionCount(guide), 0),
  withStages: summaries.filter((guide) => guide.stageCount > 0).length,
  withQuestions: summaries.filter((guide) => guide.questionCount > 0).length,
  withTips: summaries.filter((guide) => guide.tipCount > 0).length,
};

const index = Object.fromEntries(summaries.map(({ topics, tipCount, ...guide }) => [guide.slug, guide]));
const generatedTypeScript = `// Generated by scripts/generate-interview-guide-summaries.mjs. Do not edit by hand.\n\n`
  + `export interface InterviewGuideSummary {\n`
  + `  company: string;\n  slug: string;\n  scrapedAt: string;\n  stageCount: number;\n`
  + `  questionCount: number;\n  tipCount: number;\n  difficulty: number | null;\n  topics: string[];\n}\n\n`
  + `export const INTERVIEW_GUIDE_SUMMARIES: InterviewGuideSummary[] = ${JSON.stringify(summaries, null, 2)};\n\n`
  + `export const INTERVIEW_GUIDE_TOTALS = ${JSON.stringify(totals, null, 2)} as const;\n`;
const generatedIndex = `${JSON.stringify(index, null, 2)}\n`;
const check = process.argv.includes('--check');

if (check) {
  const stale = [
    [summariesPath, generatedTypeScript],
    [indexPath, generatedIndex],
  ].filter(([outputPath, expected]) => fs.readFileSync(outputPath, 'utf8') !== expected)
    .map(([outputPath]) => path.relative(root, outputPath));
  if (stale.length) throw new Error(`Generated interview-guide metadata is stale: ${stale.join(', ')}. Run npm run generate:interview-guides.`);
  console.log(`Interview guide metadata verified: ${totals.companies} companies, ${totals.questQuestions} quest questions, ${totals.stages} stages.`);
} else {
  fs.writeFileSync(summariesPath, generatedTypeScript);
  fs.writeFileSync(indexPath, generatedIndex);
  console.log(`Generated interview-guide metadata: ${totals.companies} companies, ${totals.questQuestions} quest questions, ${totals.stages} stages.`);
}
