/** Generates the private, authenticated iOS fallback catalog from Web sources. */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { buildMobileInterviewQuestionCatalog } from './mobile-interview-question-catalog.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const guideDirectory = path.join(root, 'data', 'interview-guides');
const banksPath = path.join(root, 'data', 'quest-category-banks.json');
const outputPath = path.join(root, 'functions', 'src', 'mobileInterviewGuideQuestions.generated.ts');

const guides = fs.readdirSync(guideDirectory)
  .filter((filename) => filename.endsWith('.json') && !filename.startsWith('_'))
  .map((filename) => JSON.parse(fs.readFileSync(path.join(guideDirectory, filename), 'utf8')));
const banks = JSON.parse(fs.readFileSync(banksPath, 'utf8'));
const catalog = buildMobileInterviewQuestionCatalog(guides, banks);

const output = `// Generated by scripts/generate-mobile-interview-question-catalog.mjs. Do not edit manually.\n\n`
  + `export interface MobileInterviewGuideStageQuestions {\n`
  + `  screening: readonly string[];\n`
  + `  coding: readonly string[];\n`
  + `  systemDesign: readonly string[];\n`
  + `  behavioral: readonly string[];\n`
  + `  values: readonly string[];\n`
  + `  final: readonly string[];\n`
  + `}\n\n`
  + `export interface MobileInterviewGuideQuestionSource {\n`
  + `  company: string;\n`
  + `  sourceURL: string;\n`
  + `  stageQuestions: MobileInterviewGuideStageQuestions;\n`
  + `}\n\n`
  + `export const MOBILE_INTERVIEW_GUIDE_QUESTIONS: Readonly<Record<string, MobileInterviewGuideQuestionSource>> = `
  + `${JSON.stringify(catalog, null, 2)};\n`;

fs.writeFileSync(outputPath, output);
console.log(`Generated ${Object.keys(catalog).length} mobile interview question guides → ${path.relative(root, outputPath)}`);
